#include <iostream>
using namespace std;
int main()
{
   int i, sum, large_sum;
   int add[i];
   cout << "enter i for elements range of array \n";
   cin >> i;
   large_sum = 0;
   // adding array elements
   for (i=0; i<4; i++)
   {
      cin >> add[i];
      sum = 0 + add[i];
      large_sum = sum + add[i];
   }
   cout << "The sum of elements of array is = " << large_sum;
}